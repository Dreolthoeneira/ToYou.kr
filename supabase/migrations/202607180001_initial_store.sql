create or replace function private.normalize_provider(value text)
returns text
language sql
immutable
set search_path = ''
as $$
  select case lower(coalesce(value, 'email'))
    when 'kakao' then 'Kakao'
    when 'google' then 'Google'
    when 'naver' then 'Naver'
    else 'email'
  end
$$;

create or replace function private.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  provider_name text;
begin
  provider_name := private.normalize_provider(coalesce(new.raw_app_meta_data ->> 'provider', new.raw_user_meta_data ->> 'provider'));
  insert into public.profiles (
    id, email, provider, provider_subject, display_name, phone, postal_code,
    address_line1, address_line2, created_at, updated_at
  ) values (
    new.id,
    new.email,
    provider_name,
    new.raw_app_meta_data ->> 'provider_id',
    coalesce(nullif(new.raw_user_meta_data ->> 'display_name', ''), nullif(new.raw_user_meta_data ->> 'full_name', ''), nullif(new.raw_user_meta_data ->> 'name', ''), split_part(coalesce(new.email, 'TO YOU 회원'), '@', 1)),
    coalesce(new.raw_user_meta_data ->> 'phone', ''),
    coalesce(new.raw_user_meta_data ->> 'postal_code', ''),
    coalesce(new.raw_user_meta_data ->> 'address_line1', ''),
    coalesce(new.raw_user_meta_data ->> 'address_line2', ''),
    now(),
    now()
  ) on conflict (id) do nothing;
  return new;
end;
$$;

