create or replace function private.update_store_order_status(requesting_user uuid, requested_order_id text, requested_status text)
returns jsonb
language plpgsql
security definer
set search_path = ''
as $$
declare
  selected_order public.orders%rowtype;
  selected_product public.products%rowtype;
begin
  if not private.is_admin(requesting_user) then raise exception '관리자 권한이 없습니다.'; end if;
  if requested_status not in ('paid', 'preparing', 'shipping', 'delivered', 'cancelled') then raise exception '올바른 주문 상태가 아닙니다.'; end if;

  select * into selected_order from public.orders where id = requested_order_id for update;
  if not found then raise exception '주문을 찾을 수 없습니다.'; end if;
  select * into selected_product from public.products where id = selected_order.product_id for update;

  if selected_order.status <> 'cancelled' and requested_status = 'cancelled' then
    update public.products set stock = stock + selected_order.quantity,
      status = case when status = 'soldout' then 'active' else status end, updated_at = now()
    where id = selected_order.product_id returning * into selected_product;
  elsif selected_order.status = 'cancelled' and requested_status <> 'cancelled' then
    if selected_product.stock < selected_order.quantity then raise exception '주문을 복구할 재고가 부족합니다.'; end if;
    update public.products set stock = stock - selected_order.quantity,
      status = case when stock - selected_order.quantity = 0 then 'soldout' else status end, updated_at = now()
    where id = selected_order.product_id returning * into selected_product;
  end if;

  update public.orders set status = requested_status where id = selected_order.id returning * into selected_order;
  return jsonb_build_object('order', to_jsonb(selected_order), 'product', to_jsonb(selected_product));
end;
$$;

