create or replace function private.add_cart_items(requesting_user uuid, items jsonb)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  item jsonb;
  selected_product public.products%rowtype;
  requested_quantity integer;
  selected_option text;
begin
  if requesting_user is null then raise exception '로그인이 필요합니다.'; end if;
  for item in select value from jsonb_array_elements(items)
  loop
    requested_quantity := greatest(1, least(99, coalesce((item ->> 'quantity')::integer, 1)));
    selected_option := left(trim(coalesce(item ->> 'option', '')), 200);
    select * into selected_product from public.products where id = item ->> 'productId' and status = 'active';
    if not found then raise exception '장바구니에 담을 상품을 찾을 수 없습니다.'; end if;
    if jsonb_array_length(selected_product.options_json) > 0 and not (selected_product.options_json ? selected_option) then
      raise exception '상품 옵션을 다시 선택해 주세요.';
    end if;
    if selected_product.stock < 1 then raise exception '주문 가능한 재고가 없습니다.'; end if;

    insert into public.cart_items (user_id, product_id, option_name, quantity, created_at, updated_at)
    values (requesting_user, selected_product.id, selected_option, least(requested_quantity, selected_product.stock), now(), now())
    on conflict (user_id, product_id, option_name) do update
      set quantity = least(public.cart_items.quantity + excluded.quantity, selected_product.stock), updated_at = now();
  end loop;
end;
$$;

